#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	n;
	int	x;

	n = size - 1;
	i = 0;
	while (i < size / 2)
	{
		x = tab[i];
		tab[i] = tab[n];
		tab[n] = x;
		i++;
		n--;
	}
}
